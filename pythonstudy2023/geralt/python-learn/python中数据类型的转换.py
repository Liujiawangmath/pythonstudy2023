# 将数字类型转换成字符串
num_str = str(11)
print(type(num_str), num_str)
float_str = str(13.34)
print(type(float_str), float_str)
# 将字符串转换为数字
num = int("11")
print(type(num), num)
num2 = float("11.34")
print(type(num2), num2)
# 整数转浮点数
float_str = float(11)
print(type(float_str), float_str)
# 浮点数转整数
int_num = int(11.345)
print(type(int_num), int_num)

