"""
True记为 1 False记为 0
比较运算符：==，！=，>,<,>=,<=
满足为 True，不满足为 False
"""
# 定义变量存储布尔类型的数据(大写)
bool_1 = True
bool_2 = False
print(f"bool_1变量的内容是:{bool_1}, 类型是：{type(bool_1)}")
print(f"bool_2变量的内容是:{bool_2}, 类型是：{type(bool_2)}")
# 比较运算符的使用
num1 = 10
num2 = 10
print(f"10==10的结果是：{num1 == num2}")
print(f"10!=10的结果是：{num1 != num2}")
name1 = "it"
name2 = "il"
print(f"it==il的结果是：{name1 == name2}")
